package dojo2;

public class Foto {

	private final int altura;
	private final int largura;

	public Foto(int altura, int largura) {
		this.altura = altura;
		this.largura = largura;
	}

	public int getLargura() {
		return largura;
	}

	public int getAltura() {
		return altura;
	}
}
